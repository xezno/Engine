namespace Engine.Common.MathUtils
{
    public struct Vector4d
    {
        public double x;
        public double y;
        public double z;
        public double w;

        public Vector4d(double x, double y, double z, double w)
        {
            this.x = x;
            this.y = y;
            this.z = z;
            this.w = w;
        }
    }
}
